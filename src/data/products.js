// Helper functions
export const filterProducts = (products, searchTerm, category, sortBy) => {
  let filtered = [...products];

  // Apply search filter
  if (searchTerm) {
    const search = searchTerm.toLowerCase();
    filtered = filtered.filter(product =>
      product.name.toLowerCase().includes(search) ||
      product.description.toLowerCase().includes(search) ||
      product.category.toLowerCase().includes(search) ||
      product.material.toLowerCase().includes(search)
    );
  }

  // Apply category filter
  if (category && category !== "All Products") {
    filtered = filtered.filter(product => product.category === category);
  }

  // Apply sorting
  filtered.sort((a, b) => {
    switch (sortBy) {
      case "name-asc":
        return a.name.localeCompare(b.name);
      case "name-desc":
        return b.name.localeCompare(a.name);
      case "price-asc":
        return parseFloat(a.price.replace('₹', '').replace(/,/g, '')) - 
               parseFloat(b.price.replace('₹', '').replace(/,/g, ''));
      case "price-desc":
        return parseFloat(b.price.replace('₹', '').replace(/,/g, '')) - 
               parseFloat(a.price.replace('₹', '').replace(/,/g, ''));
      case "rating":
        return b.rating - a.rating;
      case "newest":
        return b.id - a.id; // Assuming higher ID = newer
      case "featured":
      default:
        return b.featured ? 1 : -1; // Featured items first
    }
  });

  return filtered;
};

